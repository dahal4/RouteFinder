  double distanceChakrapath() {
    double totalDistance = 0;
    for (var i = 0; i < chakrapath.length - 1; i++) {
      totalDistance += calculateDistance(
          chakrapath[i]["lat"],
          chakrapath[i]["lng"],
          chakrapath[i + 1]["lat"],
          chakrapath[i + 1]["lng"]);
    }
    return totalDistance * 1000;
  }


